package topics;
public class D_DoVSWhile {
	public static void main(String[] args) {

		int j = 1;
	    while (j <5) {
	    	System.out.print(j);
	    	j++;	
	    }
	    
	    System.out.println();	
		
	    int i = 1;
		do {
			System.out.print(i);
			i++;
		}
		while (i < 5);
		
		
		int x = 6;
	    while (x <5) {
	    	System.out.print(x);
	    	x++;	
	    }
	    
	    System.out.println();	
		
	    
	    int y = 6;
		do {
			System.out.print(y);
			y++;
		}
		while (y < 5);
	}
}

